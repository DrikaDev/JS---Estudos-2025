//Loops são usados para repetir um bloco de código.
//Exemplo com for:

for (let i = 1; i <= 5; i++) {
    console.log("Número: ", i);
}

for (let i = 1; i <= 10; i++) {
    if (i % 2 === 0) {
        console.log("Número par: ", i);
    }
}

for (let i = 1; i <= 10; i++) {
    if (i % 2 !== 0) {
        console.log("Número ímpar: ", i);
    }
}